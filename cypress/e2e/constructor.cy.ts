/// <reference types="cypress" />

describe('Страница конструктора бургера', () => {
  // Хук beforeEach выполняется перед КАЖДЫМ тестом
  beforeEach(() => {
    // ПЕРЕХВАТ ЗАПРОСОВ - ключевое требование!

    // 1. Перехват запроса ингредиентов
    cy.intercept('GET', '**/api/ingredients', {
      fixture: 'ingredients.json'
    }).as('getIngredients'); // Даем имя запросу

    // 2. Перехват запроса данных пользователя (для авторизации)
    cy.intercept('GET', '**/api/auth/user', {
      fixture: 'user.json'
    }).as('getUser');

    // 3. Перехват запроса создания заказа
    cy.intercept('POST', '**/api/orders', {
      fixture: 'order.json'
    }).as('createOrder');

    // Посещаем главную страницу
    cy.visit('/');

    // Ждем, пока загрузятся ингредиенты
    cy.wait('@getIngredients');
  });

  // ============================================
  // ТЕСТ 1: Добавление ингредиентов в конструктор
  // ============================================
  describe('Добавление ингредиентов в конструктор', () => {
    it('должен добавить булку в конструктор', () => {
      // Находим булку по имени и добавляем её
      cy.contains('Краторная булка N-200i')
        .parents('li') // Поднимаемся до элемента списка
        .within(() => {
          cy.contains('Добавить').click(); // Кликаем на кнопку "Добавить"
        });

      // Проверяем, что булка появилась в конструкторе (и верх, и низ)
      cy.contains('Краторная булка N-200i (верх)').should('exist');
      cy.contains('Краторная булка N-200i (низ)').should('exist');

      // Проверяем, что булка добавилась только один раз
      cy.get('[class*="constructor-element"]').should('have.length', 2);
    });

    it('должен добавить начинку в конструктор', () => {
      // Сначала добавляем булку (обязательно для конструктора)
      cy.contains('Краторная булка N-200i')
        .parents('li')
        .within(() => {
          cy.contains('Добавить').click();
        });

      // Добавляем начинку
      cy.contains('Биокотлета из марсианской Магнолии')
        .parents('li')
        .within(() => {
          cy.contains('Добавить').click();
        });

      // Проверяем, что начинка появилась в конструкторе
      cy.contains('Биокотлета из марсианской Магнолии').should('exist');

      // Можно добавить несколько начинок
      cy.contains('Соус Spicy-X')
        .parents('li')
        .within(() => {
          cy.contains('Добавить').click();
        });

      // Проверяем, что добавились обе начинки
      cy.get('[class*="constructor-element"]').should('have.length', 4); // 2 булки + 2 начинки
    });
  });

  // ============================================
  // ТЕСТ 2: Работа модального окна ингредиента
  // ============================================
  describe('Работа модального окна ингредиента', () => {
    it('должен открыть модальное окно ингредиента при клике на него', () => {
      // Кликаем на ингредиент (не на кнопку "Добавить", а на сам ингредиент)
      cy.contains('Краторная булка N-200i').click();

      // Проверяем, что модальное окно открылось
      cy.contains('Детали ингредиента').should('exist');
      cy.contains('Краторная булка N-200i').should('exist');

      // Проверяем, что отображаются детали (калории и т.д.)
      cy.contains('Калории, ккал').should('exist');
      cy.contains('420').should('exist');
    });

    it('должен закрыть модальное окно по клику на крестик', () => {
      // Открываем модальное окно
      cy.contains('Краторная булка N-200i').click();
      cy.contains('Детали ингредиента').should('exist');

      // Ищем кнопку закрытия (крестик)
      // Можно искать по иконке CloseIcon или по роли кнопки
      cy.get('button').contains('CloseIcon').click();

      // Проверяем, что модальное окно закрылось
      cy.contains('Детали ингредиента').should('not.exist');
    });

    it('должен закрыть модальное окно по клику на оверлей', () => {
      // Открываем модальное окно
      cy.contains('Краторная булка N-200i').click();
      cy.contains('Детали ингредиента').should('exist');

      // Кликаем на оверлей (затемненную область)
      // Используем селектор по классу, содержащему "overlay"
      cy.get('[class*="overlay"]').click({ force: true });

      // Проверяем, что модальное окно закрылось
      cy.contains('Детали ингредиента').should('not.exist');
    });

    it('должен отображать данные именно того ингредиента, по которому кликнули', () => {
      // Кликаем на первый ингредиент
      cy.contains('Краторная булка N-200i').click();
      cy.contains('Детали ингредиента').should('exist');
      cy.contains('Краторная булка N-200i').should('exist');
      cy.contains('Биокотлета из марсианской Магнолии').should('not.exist');

      // Закрываем
      cy.get('button').contains('CloseIcon').click();

      // Кликаем на второй ингредиент
      cy.contains('Биокотлета из марсианской Магнолии').click();
      cy.contains('Биокотлета из марсианской Магнолии').should('exist');
      cy.contains('Краторная булка N-200i').should('not.exist');
    });
  });

  // ============================================
  // ТЕСТ 3: Создание заказа
  // ============================================
  describe('Создание заказа', () => {
    // Специальный хук для тестов заказа
    beforeEach(() => {
      // Подставляем моковые токены авторизации
      cy.setMockTokens();
    });

    afterEach(() => {
      // Очищаем токены после теста
      cy.clearMockTokens();
    });

    it('должен создать заказ и очистить конструктор', () => {
      // 1. СОБИРАЕМ БУРГЕР

      // Добавляем булку
      cy.contains('Краторная булка N-200i')
        .parents('li')
        .within(() => {
          cy.contains('Добавить').click();
        });

      // Добавляем начинку
      cy.contains('Биокотлета из марсианской Магнолии')
        .parents('li')
        .within(() => {
          cy.contains('Добавить').click();
        });

      // Добавляем соус
      cy.contains('Соус Spicy-X')
        .parents('li')
        .within(() => {
          cy.contains('Добавить').click();
        });

      // Проверяем, что ингредиенты добавились
      cy.contains('Краторная булка N-200i (верх)').should('exist');
      cy.contains('Биокотлета из марсианской Магнолии').should('exist');
      cy.contains('Соус Spicy-X').should('exist');

      // 2. ОФОРМЛЯЕМ ЗАКАЗ

      // Кликаем на кнопку оформления заказа
      cy.contains('Оформить заказ').click();

      // Ждем ответ от сервера (наш перехваченный запрос)
      cy.wait('@createOrder');

      // 3. ПРОВЕРЯЕМ МОДАЛЬНОЕ ОКНО С ЗАКАЗОМ

      // Проверяем, что модальное окно открылось
      cy.contains('идентификатор заказа').should('exist');

      // Проверяем, что номер заказа верный (из моковых данных)
      cy.contains('12345').should('exist');

      // Проверяем, что есть подтверждение
      cy.contains('Ваш заказ начали готовить').should('exist');

      // 4. ЗАКРЫВАЕМ МОДАЛЬНОЕ ОКНО

      cy.get('button').contains('CloseIcon').click();

      // Проверяем, что модальное окно закрылось
      cy.contains('идентификатор заказа').should('not.exist');

      // 5. ПРОВЕРЯЕМ, ЧТО КОНСТРУКТОР ПУСТ

      // Должны появиться сообщения о необходимости выбора ингредиентов
      cy.contains('Выберите булки').should('exist');
      cy.contains('Выберите начинку').should('exist');

      // Проверяем, что ингредиентов больше нет
      cy.contains('Краторная булка N-200i (верх)').should('not.exist');
      cy.contains('Биокотлета из марсианской Магнолии').should('not.exist');
    });
  });
});
